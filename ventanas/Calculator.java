    // Boton Mostrar operaciones
    private void tbn_mostrarOpsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbn_mostrarOpsActionPerformed
        // Crea el objeto de la ventana de las operaciones de matrices
        Operations frmOps1 = new Operations(this, true);
        
        // Si la matriz A y matriz B no esta vacias
        if(mtz1 != null && mtz2 != null)
        {
            // Cambiar el valor de la caja de texto agregando las operaciones realizadas, del objeto de matriz A, matriz B y de opMatrices
            frmOps1.setOps(mtz1.getMessage());
            frmOps1.setOps(mtz2.getMessage());
            frmOps1.setOps(opMtz.getMessage());
        }
        // Hace visible la ventana de operaciones de matrices
        frmOps1.setVisible(true);
    }//GEN-LAST:event_tbn_mostrarOpsActionPerformed

