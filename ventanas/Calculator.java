    // Boton calcular
    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        // Extrae los datos que se utilizaran para realizar una operación
        String matrizA = txt_matrizA.getText(); // Matriz A
        String escalarA = txt_escalarA.getText(); // Escalar de matriz A
        String expoA = txt_expoA.getText(); // Exponente de matriz A
        String matrizB = txt_matrizB.getText(); // Matriz B
        String escalarB = txt_escalarB.getText(); // Escalar de matriz B
        String expoB = txt_expoB.getText(); // Exponente de matriz B
        char operacion = lbl_operador.getText().charAt(0); // Operación a utilizar (Extrae '+', '-' o '*')
        
       try
       {
    	   	// Inicializa nuevos objetos. Matriz A, B y operaciones de matrices
       		mtz1 = new Matrices(matrizA, escalarA, expoA);
       		mtz2 = new Matrices(matrizB, escalarB, expoB);
            opMtz = new OpMatrices(mtz1, mtz2, operacion);
            // Modifica las variables de matriz A, B y op de matrices. Para guardarlas en las variables de esta clase
            this.setMtz(mtz1, mtz2, opMtz);
                
            // Verifica que no haya errores para poder mostrar las matrices
            if(opMtz.calcular())
            	txt_resultado.setText(opMtz.mostrarMatrices());
                
            // Modifica los label donde se muestra el tamaño de las matrices (Deajo de la caja de texto de las matrices) y muestra su tamaño.
            lbl_valor_a.setText(mtz1.getTam());
            lbl_valor_b.setText(mtz2.getTam());
            lbl_valor_axb.setText(opMtz.getTam());
       } catch(Exception e) {
    	   System.out.println("TERMINADO."); // <---- MOSTRAR EN CONSOLA.
       }
    }//GEN-LAST:event_btn_calcularActionPerformed

